package ozellikler;

public class haberEkleOzellik {

	private String haberid;
	private String haberKat;
	private String haberAdi;
	private String haberTarih;
	private String hicerik;
	
	
	
	public String getHaberid() {
		return haberid;
	}
	public void setHaberid(String haberid) {
		this.haberid = haberid;
	}
	public String getHaberKat() {
		return haberKat;
	}
	public void setHaberKat(String haberKat) {
		this.haberKat = haberKat;
	}
	public String getHaberAdi() {
		return haberAdi;
	}
	public void setHaberAdi(String haberAdi) {
		this.haberAdi = haberAdi;
	}
	public String getHaberTarih() {
		return haberTarih;
	}
	public void setHaberTarih(String haberTarih) {
		this.haberTarih = haberTarih;
	}
	public String getHicerik() {
		return hicerik;
	}
	public void setHicerik(String hicerik) {
		this.hicerik = hicerik;
	}
	
	
}
